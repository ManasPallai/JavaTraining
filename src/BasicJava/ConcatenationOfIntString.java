package BasicJava;

public class ConcatenationOfIntString {
	public static void main(String[] args) {
		System.out.println(30+5);
		//Between two numbers + will act as Arithmetic Operator//
		
		System.out.println("My Nmae is:"+"MANAS");
		//Between two Strings + will act as Concatenation Operator//
		
		System.out.println("My Age is:"+30+(5+10/2));
		//Between a number & String + will act as Concatenation Operator- BODMAS rule //
		

		System.out.println("My Age is:"+(30+10+10/2));
		
	}

}
